function y = Ber_d(x)
%Derivative of Bernoulli function
    if(abs(x)<0.58)
       y = -1/2 + x/6 - (x^3)/180 + (x^5)/5040 - (x^7)/151200 + (x^9)/4790016 - 691*(x^11)/108972864000 + (x^13)/5337446400 - 3617*(x^15)/666913927680000 + 43867*(x^17)/283838567620608000 - 174611*(x^19)/40142883134914560000; 
    else
        y = -(exp(x)*(x-1)+1)/(exp(x)-1)^2;
    end
end